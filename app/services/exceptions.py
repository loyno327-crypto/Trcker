"""Исключения сервисного слоя — чтобы хендлеры/API могли отличать ожидаемые
бизнес-ошибки (показать пользователю сообщение) от непредвиденных багов."""


class ServiceError(Exception):
    """Базовое исключение для всех ошибок сервисного слоя."""


class TaskNotFoundError(ServiceError):
    pass


class TaskNotOwnedError(ServiceError):
    """Задача существует, но принадлежит другому пользователю."""
